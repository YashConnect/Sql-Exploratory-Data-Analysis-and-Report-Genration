--Change over time
SELECT DATETRUNC(YEAR,order_date) as [year over year],sum(sales_amount) AS Sales_amount, COUNT(DISTINCT customer_key) as total_cust,sum(quantity) as total_quantity FROM Gold.fact_sales WHERE order_date is not null GROUP BY DATETRUNC(YEAR,order_date) ORDER BY [year over year];

--Cumulative analysis--
--total sales over month and running total of sales over time;
SELECT order_date,total_sales,sum(total_sales) OVER(ORDER BY order_date)runningtotal,avg(AVGPRICE) over (order by order_date) runningavg FROM(
SELECT DATETRUNC(MONTH,order_date)order_date,SUM(sales_amount)total_sales,AVG(unit_price)AVGPRICE FROM Gold.fact_sales WHERE order_date is not null GROUP BY DATETRUNC(MONTH,order_date))t;

--performance analysis
--Analyze performance of products by comparing their sales to avg sales performance of product to previous year sales
WITH yearly_product_sales AS(
SELECT YEAR(f.order_date) YEAR ,p.product_name,SUM(sales_amount)Totalsales FROM Gold.fact_sales f LEFT JOIN Gold.dim_products p ON p.product_key=f.product_key where order_date is not null group by year(f.order_date),p.product_name)
select YEAR,product_name,Totalsales,avg(Totalsales) over (partition by product_name),Totalsales-avg(Totalsales) over (partition by product_name) diff_avg,case when Totalsales-avg(Totalsales) over (partition by product_name) >0 then 'Above avg' else 'below avg' end avg_change,lag(Totalsales) over(partition by product_name order by year) py_sales ,Totalsales-lag(Totalsales) over(partition by product_name order by year) yoy_sales,case when Totalsales-lag(Totalsales) over(partition by product_name order by year) >0 then 'increasing' when Totalsales-lag(Totalsales) over(partition by product_name order by year)<0 then 'decreasing' else 'same' end pysales_change  from yearly_product_sales order by 2,1;


--With subquery
SELECT YEAR,product_name,Totalsales,avg(Totalsales) over(partition by product_name) as avgsales,Totalsales-avg(Totalsales) over(partition by product_name),lag(Totalsales) over (partition by product_name ORDER BY year),case when Totalsales-avg(Totalsales) over(partition by product_name) >0 then 'avgsale' else 'belowavg' end type from (SELECT YEAR(f.order_date) YEAR ,p.product_name,SUM(sales_amount)Totalsales FROM Gold.fact_sales f LEFT JOIN Gold.dim_products p ON p.product_key=f.product_key where order_date is not null group by year(f.order_date),p.product_name)t;

--part to whole analysis
--Category wise analysis 
SELECT 
p.category,
sum(s.sales_amount),
SUM(sum(s.sales_amount)) over() total_sales,
concat(round((cast(sum(s.sales_amount)as float)/SUM(sum(s.sales_amount))over())*100,2),'%') as percentage
FROM Gold.fact_sales s
left join 
Gold.dim_products p
on s.product_key=p.product_key 
group by p.category

--data segmentation
--Segment product into cost ranges and count how many product fall in each segment
select segment,count(*) totalproducts from(select product_name,product_cost,case when product_cost>0 and product_cost<=100 then 'below 100' when product_cost between 100 and 500 then '100-500' when product_cost between 501 and 1000 then '501-1000' else 'above 1000' end segment from Gold.dim_products)t group by segment order by totalproducts desc

--Segment customers
with customer_spending as (
SELECT c.customer_key,sum(s.sales_amount)total_sales,min(s.order_date)first_order,max(s.order_date) last_order,DATEDIFF(MONTH,min(s.order_date),max(s.order_date))lifespan,case when DATEDIFF(MONTH,min(s.order_date),max(s.order_date))>=12 and sum(s.sales_amount)>5000 then 'Vip' when DATEDIFF(MONTH,min(s.order_date),max(s.order_date))>=12 and sum(s.sales_amount)<=5000 then 'Regular' else 'New' end customer_segment from Gold.fact_sales s left join Gold.dim_customers c on c.customer_key=s.customer_key group by c.customer_key)
select customer_segment,count(*) total_cust from customer_spending group by customer_segment order by total_cust desc

/*

Customer Report

Purpose:
- This report consolidates key customer metrics and behaviors

Highlights:
1. Gathers essential fields such as names, ages, and transaction details.
2. Segments customers into categories (VIP, Regular, New) and age groups.
3. Aggregates customer-level metrics:
- total orders
- total sales
- total quantity purchased
- total products
- lifespan (in months)
4. Calculates valuable KPIs:
- recency (months since last order)
- average order value
- average monthly spend

*/

SELECT s.order_number,p.product_key,s.order_date,s.sales_amount,s.quantity,c.customer_key,c.customer_number,CONCAT(c.first_name,' ',c.last_name) full_name,CASE WHEN DATEDIFF(YEAR,birthday,GETDATE())>=18 and DATEDIFF(YEAR,birthday,GETDATE())<=30 then '18-30' when DATEDIFF(YEAR,birthday,GETDATE())>=31 and DATEDIFF(YEAR,birthday,GETDATE())<=40 then '31-40' else '40 above' end age_group  FROM Gold.fact_sales s left join Gold.dim_customers c  on c.customer_key=s.customer_key left join  Gold.dim_products p on p.product_key=s.product_key where s.order_date is not null;