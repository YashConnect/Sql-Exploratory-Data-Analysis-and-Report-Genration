--Measures--
--TOTAL SALES--
SELECT SUM(sales_amount) AS Total_sales FROM Gold.fact_sales;

--TOTAL ITEMS--
SELECT SUM(quantity) AS Total_items FROM Gold.fact_sales;

--AVG PRICE--
SELECT AVG(unit_price) AS Average_price FROM Gold.fact_sales;

--TOTAL ORDERS--
SELECT COUNT(DISTINCT order_number) FROM Gold.fact_sales;

--TOTAL PRODUCTS--
SELECT COUNT(*) FROM Gold.dim_products;

--TOTAL CUSTOMERS--
SELECT COUNT(*) FROM Gold.dim_customers;

--TOTAL CUSTOMER PLACED ORDER 
SELECT COUNT (DISTINCT customer_key) FROM Gold.fact_sales;

--ALL TOGETHER 
SELECT 'Total_sales' AS Measure_name,SUM(sales_amount) AS Value FROM Gold.fact_sales
UNION ALL
SELECT 'Total_quantity' AS Measure_name,SUM(quantity) AS Value FROM Gold.fact_sales
UNION ALL
SELECT 'Average_price' AS Measure_name,AVG(unit_price) AS Value FROM Gold.fact_sales
UNION ALL
SELECT 'Total_orders' AS Measure_name,COUNT(DISTINCT order_number)AS Value FROM Gold.fact_sales
UNION ALL
SELECT 'Total_products' AS Measure_name,COUNT(*)AS Value FROM Gold.dim_products
UNION ALL
SELECT 'Total_customers' AS Measure_name,COUNT(*)AS Value FROM Gold.dim_customers;

--Magnitude
--Total customers by country
SELECT country,COUNT(*)AS No_of_cust FROM Gold.dim_customers GROUP BY country ORDER BY No_of_cust DESC;
--Total customers by gender
SELECT gender,COUNT(*)AS No_of_cust FROM Gold.dim_customers GROUP BY gender ORDER BY No_of_cust DESC;
--Total products by category
SELECT category,COUNT(*)AS No_of_prod FROM Gold.dim_products GROUP BY category ORDER BY No_of_prod DESC;
--Avg cost in each category
SELECT category,AVG(product_cost)AS Avg_cost FROM Gold.dim_products GROUP BY category ORDER BY Avg_cost DESC;
--Total revenue generated by each category
SELECT dp.category,SUM(fs.sales_amount)AS Total_revenue FROM Gold.fact_sales fs LEFT JOIN Gold.dim_products dp ON fs.product_key=dp.product_key GROUP BY dp.category ORDER BY Total_revenue DESC;

-- What is the total revenue generated by each customer?
SELECT
c.customer_key,
c.first_name,
c.last_name,
SUM(f.sales_amount) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON c.customer_key = f.customer_key
GROUP BY
c.customer_key,
c.first_name,
c.last_name
ORDER BY total_revenue DESC

-- What is the distribution of sold items across countries?

SELECT
c.country,
SUM(f.quantity) AS total_sold_quantity
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON c.customer_key = f.customer_key
GROUP BY
c.country
ORDER BY total_sold_quantity DESC

